package com.cyient.methods;

public class Area {
//method area of circle
	public static double areaOfCircle(int r) {
		double area = 3.14 * r * r;
		return area;
	}
//area of rectangle
	public static int areaOfRectangle(int length, int width) {
		return length * width;
	}
//area of triangle


//area if Square

public static double areaOfSqare(int side) {
	return side*side;
}

//area of trapeziumn a+b/2*h

public static double areaOfSqare(int a,int b, int h) {
	return a+b/2*h;
}
}