package com.cyient.javaassignment;

public class FirstProblem {

	public static void main(String[] args) {
		// 2. Create a program to print the sequence
		// 3 x 1 = 3
		// 3 x 2 = 6
		// 3 x 20 = 60
		int i, x;

		for (i = 1; i <= 20; i++) {

			x = 3 * i;
			System.out.println(x);
		}

	}

}
